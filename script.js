// ========== 贪吃蛇游戏逻辑 ==========
class SnakeGame {
    constructor(canvasId) {
        this.canvas = document.getElementById(canvasId);
        if (!this.canvas) {
            console.error('游戏画布未找到:', canvasId);
            return;
        }
        
        this.ctx = this.canvas.getContext('2d');
        this.gridSize = 20;
        this.snake = [];
        this.food = { x: 0, y: 0 };
        this.direction = 'right';
        this.nextDirection = 'right';
        this.score = 0;
        this.highScore = parseInt(localStorage.getItem('snakeHighScore')) || 0;
        this.level = 1;
        this.speed = 150; // 初始速度（毫秒）
        this.gameInterval = null;
        this.isPaused = false;
        this.gameRunning = false;
        
        // 确保画布有正确的尺寸
        this.resizeCanvas();
        
        // 监听窗口大小变化
        window.addEventListener('resize', () => {
            this.resizeCanvas();
            if (this.gameRunning) {
                this.draw();
            }
        });
        
        // 初始化游戏
        this.init();
    }
    
    resizeCanvas() {
        if (!this.canvas || !this.canvas.parentElement) return;
        
        const container = this.canvas.parentElement;
        this.canvas.width = container.clientWidth;
        this.canvas.height = container.clientHeight;
        
        console.log('画布尺寸调整:', this.canvas.width, this.canvas.height);
    }
    
    init() {
        if (!this.canvas) return;
        
        // 初始化蛇的位置
        this.snake = [
            { x: 10, y: 10 },
            { x: 9, y: 10 },
            { x: 8, y: 10 }
        ];
        
        // 生成第一个食物
        this.generateFood();
        
        // 绘制初始游戏状态
        this.draw();
        
        // 更新高分显示
        this.updateHighScoreDisplay();
        
        // 更新难度显示
        this.updateDifficultyDisplay();
    }
    
    updateHighScoreDisplay() {
        const highScoreElement = document.getElementById('highScore');
        if (highScoreElement) {
            highScoreElement.textContent = this.highScore;
        }
    }
    
    updateDifficultyDisplay() {
        const difficultyElement = document.getElementById('difficultyValue');
        const difficultySelect = document.getElementById('difficulty');
        
        if (difficultyElement && difficultySelect) {
            // 获取当前选择的难度选项
            const selectedOption = difficultySelect.options[difficultySelect.selectedIndex];
            difficultyElement.textContent = selectedOption.text;
        }
    }
    
    start() {
        if (this.gameRunning && !this.isPaused) return;
        
        this.gameRunning = true;
        this.isPaused = false;
        
        console.log('游戏开始');
        
        // 隐藏开始屏幕
        const gameStartScreen = document.getElementById('gameStart');
        if (gameStartScreen) {
            gameStartScreen.style.display = 'none';
        }
        
        // 隐藏游戏结束屏幕
        const gameOverScreen = document.getElementById('gameOver');
        if (gameOverScreen) {
            gameOverScreen.style.display = 'none';
        }
        
        // 启用暂停按钮
        const pauseBtn = document.getElementById('pauseGame');
        if (pauseBtn) {
            pauseBtn.disabled = false;
        }
        
        // 设置游戏循环
        if (this.gameInterval) clearInterval(this.gameInterval);
        
        // 根据难度设置速度
        const difficulty = document.getElementById('difficulty')?.value || 'medium';
        this.setSpeedByDifficulty(difficulty);
        
        this.gameInterval = setInterval(() => {
            if (!this.isPaused) {
                this.update();
                this.draw();
            }
        }, this.speed);
    }
    
    pause() {
        this.isPaused = !this.isPaused;
        const pauseBtn = document.getElementById('pauseGame');
        
        if (pauseBtn) {
            const pauseIcon = pauseBtn.querySelector('i');
            const pauseText = pauseBtn.querySelector('span');
            
            if (this.isPaused) {
                if (pauseIcon) pauseIcon.className = 'fas fa-play';
                if (pauseText) pauseText.textContent = '继续';
                console.log('游戏暂停');
            } else {
                if (pauseIcon) pauseIcon.className = 'fas fa-pause';
                if (pauseText) pauseText.textContent = '暂停';
                console.log('游戏继续');
            }
        }
    }
    
    restart() {
        console.log('游戏重新开始');
        
        // 重置游戏状态
        this.score = 0;
        this.level = 1;
        this.direction = 'right';
        this.nextDirection = 'right';
        this.isPaused = false;
        this.gameRunning = false;
        
        // 更新显示
        const scoreElement = document.getElementById('score');
        const levelElement = document.getElementById('level');
        if (scoreElement) scoreElement.textContent = this.score;
        if (levelElement) levelElement.textContent = this.level;
        
        // 重置按钮状态
        const pauseBtn = document.getElementById('pauseGame');
        if (pauseBtn) {
            pauseBtn.disabled = true;
            const icon = pauseBtn.querySelector('i');
            const text = pauseBtn.querySelector('span');
            if (icon) icon.className = 'fas fa-pause';
            if (text) text.textContent = '暂停';
        }
        
        // 隐藏游戏结束屏幕
        const gameOverScreen = document.getElementById('gameOver');
        if (gameOverScreen) {
            gameOverScreen.style.display = 'none';
        }
        
        // 显示开始屏幕
        const gameStartScreen = document.getElementById('gameStart');
        if (gameStartScreen) {
            gameStartScreen.style.display = 'flex';
        }
        
        // 更新难度显示
        this.updateDifficultyDisplay();
        
        // 重新初始化游戏
        this.init();
        
        // 清除游戏循环
        if (this.gameInterval) {
            clearInterval(this.gameInterval);
            this.gameInterval = null;
        }
        
        console.log('游戏重置完成');
    }
    
    setSpeedByDifficulty(difficulty) {
        switch(difficulty) {
            case 'easy':
                this.speed = 200;
                break;
            case 'medium':
                this.speed = 150;
                break;
            case 'hard':
                this.speed = 100;
                break;
            case 'expert':
                this.speed = 70;
                break;
            default:
                this.speed = 150;
        }
        console.log('游戏速度设置:', difficulty, this.speed);
    }
    
    generateFood() {
        if (!this.canvas) return;
        
        const gridWidth = Math.floor(this.canvas.width / this.gridSize);
        const gridHeight = Math.floor(this.canvas.height / this.gridSize);
        
        // 生成不在蛇身上的随机位置
        let foodOnSnake;
        let attempts = 0;
        const maxAttempts = 100;
        
        do {
            foodOnSnake = false;
            attempts++;
            
            this.food.x = Math.floor(Math.random() * gridWidth);
            this.food.y = Math.floor(Math.random() * gridHeight);
            
            // 检查食物是否在蛇身上
            for (let segment of this.snake) {
                if (segment.x === this.food.x && segment.y === this.food.y) {
                    foodOnSnake = true;
                    break;
                }
            }
            
            // 防止无限循环
            if (attempts > maxAttempts) {
                console.warn('生成食物失败，尝试次数过多');
                break;
            }
        } while (foodOnSnake);
    }
    
    update() {
        if (!this.canvas) return;
        
        // 更新方向
        this.direction = this.nextDirection;
        
        // 根据方向计算新头部位置
        const head = { ...this.snake[0] };
        
        switch(this.direction) {
            case 'up':
                head.y -= 1;
                break;
            case 'down':
                head.y += 1;
                break;
            case 'left':
                head.x -= 1;
                break;
            case 'right':
                head.x += 1;
                break;
        }
        
        const gridWidth = Math.floor(this.canvas.width / this.gridSize);
        const gridHeight = Math.floor(this.canvas.height / this.gridSize);
        
        // 检查是否撞墙
        if (head.x < 0 || head.x >= gridWidth ||
            head.y < 0 || head.y >= gridHeight) {
            this.gameOver();
            return;
        }
        
        // 检查是否撞到自己
        for (let segment of this.snake) {
            if (head.x === segment.x && head.y === segment.y) {
                this.gameOver();
                return;
            }
        }
        
        // 添加新头部
        this.snake.unshift(head);
        
        // 检查是否吃到食物
        if (head.x === this.food.x && head.y === this.food.y) {
            // 加分
            this.score += 10;
            const scoreElement = document.getElementById('score');
            if (scoreElement) {
                scoreElement.textContent = this.score;
            }
            
            // 检查是否升级
            const newLevel = Math.floor(this.score / 500) + 1;
            if (newLevel > this.level) {
                this.level = newLevel;
                const levelElement = document.getElementById('level');
                if (levelElement) {
                    levelElement.textContent = this.level;
                }
                
                // 每升一级速度加快
                if (this.speed > 50) {
                    this.speed -= 10;
                    if (this.gameInterval) {
                        clearInterval(this.gameInterval);
                        this.gameInterval = setInterval(() => {
                            if (!this.isPaused) {
                                this.update();
                                this.draw();
                            }
                        }, this.speed);
                    }
                }
            }
            
            // 生成新食物
            this.generateFood();
        } else {
            // 如果没有吃到食物，移除尾部
            this.snake.pop();
        }
    }
    
    draw() {
        if (!this.canvas || !this.ctx) return;
        
        // 清空画布
        this.ctx.fillStyle = '#1a1a2e';
        this.ctx.fillRect(0, 0, this.canvas.width, this.canvas.height);
        
        // 绘制网格（可选）
        this.drawGrid();
        
        // 绘制蛇
        this.snake.forEach((segment, index) => {
            this.ctx.fillStyle = index === 0 ? '#4CAF50' : '#8BC34A'; // 头部和身体不同颜色
            this.ctx.fillRect(
                segment.x * this.gridSize,
                segment.y * this.gridSize,
                this.gridSize - 1,
                this.gridSize - 1
            );
            
            // 绘制蛇眼睛（只在头部）
            if (index === 0) {
                this.ctx.fillStyle = '#000';
                const eyeSize = 4;
                
                // 根据方向绘制眼睛
                if (this.direction === 'right') {
                    this.ctx.fillRect(
                        segment.x * this.gridSize + this.gridSize - 6,
                        segment.y * this.gridSize + 4,
                        eyeSize, eyeSize
                    );
                    this.ctx.fillRect(
                        segment.x * this.gridSize + this.gridSize - 6,
                        segment.y * this.gridSize + this.gridSize - 8,
                        eyeSize, eyeSize
                    );
                } else if (this.direction === 'left') {
                    this.ctx.fillRect(
                        segment.x * this.gridSize + 2,
                        segment.y * this.gridSize + 4,
                        eyeSize, eyeSize
                    );
                    this.ctx.fillRect(
                        segment.x * this.gridSize + 2,
                        segment.y * this.gridSize + this.gridSize - 8,
                        eyeSize, eyeSize
                    );
                } else if (this.direction === 'up') {
                    this.ctx.fillRect(
                        segment.x * this.gridSize + 4,
                        segment.y * this.gridSize + 2,
                        eyeSize, eyeSize
                    );
                    this.ctx.fillRect(
                        segment.x * this.gridSize + this.gridSize - 8,
                        segment.y * this.gridSize + 2,
                        eyeSize, eyeSize
                    );
                } else if (this.direction === 'down') {
                    this.ctx.fillRect(
                        segment.x * this.gridSize + 4,
                        segment.y * this.gridSize + this.gridSize - 6,
                        eyeSize, eyeSize
                    );
                    this.ctx.fillRect(
                        segment.x * this.gridSize + this.gridSize - 8,
                        segment.y * this.gridSize + this.gridSize - 6,
                        eyeSize, eyeSize
                    );
                }
            }
        });
        
        // 绘制食物
        this.ctx.fillStyle = '#FF6347';
        this.ctx.beginPath();
        const centerX = this.food.x * this.gridSize + this.gridSize / 2;
        const centerY = this.food.y * this.gridSize + this.gridSize / 2;
        const radius = this.gridSize / 2 - 2;
        this.ctx.arc(centerX, centerY, radius, 0, Math.PI * 2);
        this.ctx.fill();
        
        // 添加食物光泽效果
        this.ctx.fillStyle = '#FFA07A';
        this.ctx.beginPath();
        this.ctx.arc(centerX - radius/3, centerY - radius/3, radius/3, 0, Math.PI * 2);
        this.ctx.fill();
    }
    
    drawGrid() {
        if (!this.canvas || !this.ctx) return;
        
        this.ctx.strokeStyle = 'rgba(255, 255, 255, 0.05)';
        this.ctx.lineWidth = 1;
        
        // 绘制垂直线
        for (let x = 0; x <= this.canvas.width; x += this.gridSize) {
            this.ctx.beginPath();
            this.ctx.moveTo(x, 0);
            this.ctx.lineTo(x, this.canvas.height);
            this.ctx.stroke();
        }
        
        // 绘制水平线
        for (let y = 0; y <= this.canvas.height; y += this.gridSize) {
            this.ctx.beginPath();
            this.ctx.moveTo(0, y);
            this.ctx.lineTo(this.canvas.width, y);
            this.ctx.stroke();
        }
    }
    
    gameOver() {
        console.log('游戏结束，得分:', this.score);
        
        // 停止游戏循环
        clearInterval(this.gameInterval);
        this.gameRunning = false;
        
        // 更新最高分
        if (this.score > this.highScore) {
            this.highScore = this.score;
            localStorage.setItem('snakeHighScore', this.highScore);
            this.updateHighScoreDisplay();
        }
        
        // 显示最终分数
        const finalScoreElement = document.getElementById('finalScore');
        if (finalScoreElement) {
            finalScoreElement.textContent = this.score;
        }
        
        // 显示游戏结束屏幕
        const gameOverScreen = document.getElementById('gameOver');
        if (gameOverScreen) {
            gameOverScreen.style.display = 'flex';
        }
        
        // 禁用暂停按钮
        const pauseBtn = document.getElementById('pauseGame');
        if (pauseBtn) {
            pauseBtn.disabled = true;
        }
    }
    
    changeDirection(newDirection) {
        // 防止直接反向移动
        if ((newDirection === 'up' && this.direction !== 'down') ||
            (newDirection === 'down' && this.direction !== 'up') ||
            (newDirection === 'left' && this.direction !== 'right') ||
            (newDirection === 'right' && this.direction !== 'left')) {
            this.nextDirection = newDirection;
        }
    }
}

